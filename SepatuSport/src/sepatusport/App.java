    //Methode Searching
    public List<sepatu> searchFromDatabase(String keyword) {
        List<sepatu> results = new ArrayList<>();

        Connection connection = null;
        PreparedStatement statement = null;
        ResultSet resultSet = null;

        try {
            // Mengambil koneksi dari dbConnection
            connection = dbConnection.getConnection();

            // Membuat statement SQL dengan parameter
            String query = "SELECT * FROM sepatu WHERE id LIKE ? OR tipe LIKE ? OR merek LIKE ? OR model LIKE ? OR warna LIKE ?";
            statement = connection.prepareStatement(query);
            statement.setString(1, "%" + keyword + "%");
            statement.setString(2, "%" + keyword + "%");
            statement.setString(3, "%" + keyword + "%");
            statement.setString(4, "%" + keyword + "%");
            statement.setString(5, "%" + keyword + "%");

            // Menjalankan pernyataan SQL
            resultSet = statement.executeQuery();

            // Mengambil hasil pencarian dan menambahkannya ke dalam daftar hasil
            while (resultSet.next()) {
                String id = resultSet.getString("id");
                String merek = resultSet.getString("merek");
                String tipe = resultSet.getString("tipe");
                String model = resultSet.getString("model");
                String warna = resultSet.getString("warna");
                int ukuran = resultSet.getInt("ukuran");
                int harga = resultSet.getInt("harga");

                sepatu data = new sepatu(id, merek, tipe, model, warna, ukuran, harga);
                results.add(data);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        } finally {
            // Menutup statement, result set, dan koneksi
            try {
                if (resultSet != null) {
                    resultSet.close();
                }
                if (statement != null) {
                    statement.close();
                }
                if (connection != null) {
                    connection.close();
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }

        return results;
    }
    
