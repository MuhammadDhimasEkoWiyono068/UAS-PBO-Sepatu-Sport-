    //Refresh Tabel
    private void updateTableData() {
        try {
            // Menghapus semua baris data sebelum melakukan refresh
            tableModel.setRowCount(0);

            Connection connection = dbConnection.getConnection();
            Statement statement = connection.createStatement();
            String query = "SELECT * FROM sepatu";
            ResultSet resultSet = statement.executeQuery(query);

            while (resultSet.next()) {
                String id = resultSet.getString("id");
                String merek = resultSet.getString("merek");
                String tipe = resultSet.getString("tipe");
                String model = resultSet.getString("model");
                String warna = resultSet.getString("warna");
                int ukuran = resultSet.getInt("ukuran");
                int harga = resultSet.getInt("harga");

                // Menambahkan data ke dalam DefaultTableModel
                tableModel.addRow(new Object[]{id, merek, tipe, model, warna, ukuran, harga});
            }

            resultSet.close();
            statement.close();
            connection.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    
